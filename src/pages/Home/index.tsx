import {
  CountdownContainer,
  FormContainer,
  HomeContainer,
  MinutesAmountInput,
  Separator,
  StartCountdownButton,
  TaskInput,
} from "./styles"
import { useState } from "react"

interface NewCycleFormData {
  task: string
  minutesAmount: number
}

interface Cycle extends NewCycleFormData {
  id: string
}

